package Ej_3;

public class Direccion {
	
	private String calle;
	private String ciudad;
	private int cpostal;
	private String pais;
	
	Direccion(){
		
	}
	Direccion(String calle,String ciudad,int cpostal,String pais){
		this.calle=calle;
		this.ciudad=ciudad;
		this.cpostal=cpostal;
		this.pais=pais;
	}
	
}
